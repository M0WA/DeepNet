size_t NamedCharacterReferences::IsRef(const char* p,const size_t size, bool& isPossibleRef, bool& isCompleteRef) {

	InitStaticNamedCharRefs();

	isPossibleRef = isCompleteRef = false;

	std::string tmp;
	tmp.append(p,size);

	std::vector<std::string>::const_iterator posRef = vecNamedCharRefs.begin();
	for(;posRef != vecNamedCharRefs.end();++posRef) {
		size_t pos = posRef->find(tmp);
		if(pos == 0) {
			isPossibleRef = true;
			isCompleteRef = posRef->length() == size;
			return size;
		}
	}
	return 0;
}

